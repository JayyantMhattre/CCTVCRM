using FluentValidation;
using MediatR;

namespace Ashraak.BuildingBlocks.Application.Behaviors;

/// <summary>
/// Shared MediatR pipeline behavior that runs all registered FluentValidation validators
/// before the request handler. Any validation failure short-circuits the pipeline.
/// Each module's Application project references this instead of duplicating the logic.
/// </summary>
public sealed class ValidationBehavior<TRequest, TResponse>(IEnumerable<IValidator<TRequest>> validators)
    : IPipelineBehavior<TRequest, TResponse>
    where TRequest : IRequest<TResponse>
{
    public async Task<TResponse> Handle(
        TRequest request,
        RequestHandlerDelegate<TResponse> next,
        CancellationToken cancellationToken)
    {
        if (!validators.Any())
            return await next();

        var context = new ValidationContext<TRequest>(request);
        var failures = validators
            .Select(v => v.Validate(context))
            .SelectMany(r => r.Errors)
            .Where(f => f is not null)
            .ToList();

        if (failures.Count != 0)
            throw new ValidationException(failures);

        return await next();
    }
}
